class HeroStatus {
  HeroStatus({
    required this.hp,
    required this.maxHp,
    required this.mp,
    required this.maxMp,
    required this.strength,
    required this.dexterity,
    required this.intelligence,
    required this.level,
    required this.experience,
    required this.nextExperience,
    required this.gold,
  });
  final int hp;
  final int maxHp;
  final int mp;
  final int maxMp;
  final int strength;
  final int dexterity;
  final int intelligence;
  final int level;
  final int experience;
  final int nextExperience;
  final int gold;
}
